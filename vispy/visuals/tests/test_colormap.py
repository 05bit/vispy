"""
Tests for texture-based colormap
All images are of size (10,10) to keep a small file size
"""

import numpy as np

from vispy.scene.visuals import Image
from vispy.testing import (requires_application, TestingCanvas,
                           run_tests_if_main)
from vispy.testing.image_tester import assert_image_match
from vispy.color import (get_colormap, Colormap)

size = (10, 10)


@requires_application()
def test_colormap_gray():
    """Test grayscale colormap"""
    with TestingCanvas(size=size, bgcolor='w') as c:
        image = Image(cmap='grays', clim=[0, 1], parent=c.scene)
        shape = (size[0], size[1])
        np.random.seed(379823)
        data = np.random.rand(*shape)
        image.set_data(data)
        assert_image_match(c.render(), img)
#        print repr(c.render())


@requires_application()
def test_colormap():
    """Test colormap support for non-uniformly distributed control-points"""
    with TestingCanvas(size=size, bgcolor='w') as c:
        idata = np.linspace(255, 0, size[0]*size[1]).astype(np.ubyte)
        data = idata.reshape((size[0], size[1]))
        image = Image(cmap=Colormap(colors=['k', 'w', 'r'],
                      controls=[0.0, 0.1, 1.0]),
                      clim='auto', parent=c.scene)
        image.set_data(data)
        assert_image_match(c.render(), kwr)


